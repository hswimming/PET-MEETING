CREATE TABLE DOG_INFO (
	ID VARCHAR2(50),
	IMG	VARCHAR2(4000),
	NAME VARCHAR2(100) NOT NULL,
	KIND VARCHAR2(100)	NOT NULL,
	SIZE CHAR(4) NOT NULL,
	GENDER CHAR(4) NOT NULL,
	NEUTERED CHAR(1) DEFAULT 'N' NOT NULL,
	VACCINE	VARCHAR2(1000) NOT NULL,
	MEMBER_CODE	VARCHAR2(20) NOT NULL,
    CONSTRAINT DOG_INFO_ID_PK PRIMARY KEY(ID)
--    CONSTRAINT MEMBER_MEMBER_CODE_FK FOREIGN KEY (MEMBER_CODE) REFERENCES MEMBER
);

COMMENT ON COLUMN DOG_INFO.ID IS '아이디';
COMMENT ON COLUMN DOG_INFO.IMG IS '사진';
COMMENT ON COLUMN DOG_INFO.NAME IS '이름';
COMMENT ON COLUMN DOG_INFO.KIND IS '견종';
COMMENT ON COLUMN DOG_INFO.SIZE IS '크기';
COMMENT ON COLUMN DOG_INFO.GENDER IS '성별';
COMMENT ON COLUMN DOG_INFO.NEUTERED IS '백신접종';
COMMENT ON COLUMN DOG_INFO.MEMBER_CODE IS '회원코드';

